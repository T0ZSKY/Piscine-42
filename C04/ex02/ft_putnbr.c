#include <unistd.h>
void	ft_putnbr(int nb)
{
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
	}
	char c;
	c = '0' + nb % 10;
	write(1, &c, 1);
}

int	main()
{
	ft_putnbr(0);
}
